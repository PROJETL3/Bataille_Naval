public class Coordonnee {
	public int x;
	public int y;
	
	public Coordonnee (int x ,int y){
		this.x=x;
		this.y=y;
		}
	
	
	

	public int getX(){
		return this.x ;
	}	
	public int getY(){
		return this.y;
	}	
	public void SetX(int x){
		this.x=x;
	}
	
	public void SetY(int y){
		this.y=y;
	}
	
	}

